
function arrayStuff () {
    myArray = [1, 2, 3, 4, 5, 6, 7];
    myArray.push('hello'); // add a new element to the end of the array
    myArray.pop(); // remove the last element from the array
    myArray.shift(); // remove the first element from the array
    myArray.reverse(); // reverse the array
    console.log(myArray);
}

function main() {
    arrayStuff();
}

main();
